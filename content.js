function walk(node) {
    let child, next;
    const skip = ['SCRIPT', 'STYLE', 'TEXTAREA', 'INPUT', 'NOSCRIPT', 'CODE', 'PRE'];
    if (node.parentElement && skip.includes(node.parentElement.tagName)) return;

    switch (node.nodeType) {
        case 1: case 9: case 11:
            child = node.firstChild;
            while (child) {
                next = child.nextSibling;
                walk(child);
                child = next;
            }
            break;
        case 3:
            if (node.nodeValue.trim().length > 3) {
                node.nodeValue = TOXIC_ENGINE.process(node.nodeValue);
            }
            break;
    }
}

